# vi 명령어
i : 현 줄에서 입력모드로 변경
a : 현 커서 다음 줄
o : 다음 줄 부터 입력
x : 현 커서 한개만 삭제
dd : 한 줄 삭제 
yy : 현 줄 복사
p : 복사한 줄 붙여넣기

esc : 명령모드로 돌아가기
:q! : 강제종료(저장x)
:wq : 저장 종료
gg : 첫 줄 이동


